// Arithmetic Operators Demo

// 1. Addition
console.log("Addition: 5 + 4 =", 5 + 4);

// 2. Subtraction
console.log("Subtraction: 5 - 2 =", 5 - 2);

// 3. Multiplication
console.log("Multiplication: 5 * 3 =", 5 * 3);

// 4. Division
console.log("Division: 10 / 2 =", 10 / 2);
console.log("Division with remainder: 7 / 2 =", 7 / 2);

// 5. Modulus (remainder)
console.log("Modulus: 10 % 3 =", 10 % 3);
console.log("Modulus: 7 % 2 =", 7 % 2);

// 6. Exponentiation
console.log("Exponentiation: 2 ** 3 =", 2 ** 3);
console.log("Exponentiation: 10 ** 2 =", 10 ** 2);

// 7. Increment
let x = 5;
console.log("Initial x =", x);
x++;
console.log("After x++ =", x);

let y = 5;
console.log("Initial y =", y);
console.log("++y =", ++y); // increments before using

// 8. Decrement
let z = 5;
console.log("Initial z =", z);
z--;
console.log("After z-- =", z);

let w = 5;
console.log("Initial w =", w);
console.log("--w =", --w); // decrements before using